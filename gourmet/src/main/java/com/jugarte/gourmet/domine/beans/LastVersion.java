package com.jugarte.gourmet.domine.beans;

public class LastVersion {

    private String nameTagVersion = null;
    private String nameVersion = null;
    private String nameDownload = null;
    private String urlDownload = null;
    private String changelog = null;
    private String urlHomePage = null;

    public String getNameTagVersion() {
        return nameTagVersion;
    }

    public void setNameTagVersion(String nameTagVersion) {
        this.nameTagVersion = nameTagVersion;
    }

    public String getNameVersion() {
        return nameVersion;
    }

    public void setNameVersion(String nameVersion) {
        this.nameVersion = nameVersion;
    }

    public String getNameDownload() {
        return nameDownload;
    }

    public void setNameDownload(String nameDownload) {
        this.nameDownload = nameDownload;
    }

    public String getUrlDownload() {
        return urlDownload;
    }

    public void setUrlDownload(String urlDownload) {
        this.urlDownload = urlDownload;
    }

    public String getChangelog() {
        return changelog;
    }

    public void setChangelog(String changelog) {
        this.changelog = changelog;
    }

    public String getUrlHomePage() {
        return urlHomePage;
    }

    public void setUrlHomePage(String urlHomePage) {
        this.urlHomePage = urlHomePage;
    }
}
